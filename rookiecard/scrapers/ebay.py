    # 第 2 层: HTML 解析
    # ===================================================================

    @staticmethod
    def parse_price(text: str) -> float | None:
        """从价格文本中提取数值。

        处理的格式:
        - "$123.45"       → 123.45
        - "$1,234.56"     → 1234.56
        - "C $85.00"      → 85.00   （加元等其他币种前缀）
        - "$100.00 to $200.00" → 100.00 （取区间最低价）
        - "Free"          → None    （无效）

        逻辑:
        1. 用正则 \\d[\\d,]*\\.?\\d* 匹配第一个数字串
        2. 去掉逗号
        3. 转为 float
        """
        if not text:
            return None
        # 匹配第一个看起来像价格的数字（整数部分可能有逗号，小数部分可选）
        match = re.search(r"(\d[\d,]*\.?\d*)", text)
        if not match:
            return None
        price_str = match.group(1).replace(",", "")
        try:
            return float(price_str)
        except ValueError:
            return None

    @staticmethod
    def parse_date(text: str) -> str | None:
        """从日期文本中提取 ISO 日期。

        eBay 的日期格式不统一，常见的有:
        - "Sold  Jan 15, 2026"  → "2026-01-15"
        - "Sold  1/15/2026"     → "2026-01-15"
        - "Jan 15, 2026"        → "2026-01-15"

        逻辑:
        1. 去掉 "Sold" 前缀和多余空格
        2. 依次尝试多种日期格式解析
        3. 返回 ISO 格式字符串
        """
        if not text:
            return None

        # 清理文本
        cleaned = re.sub(r"(?i)sold\s*", "", text).strip()

        # 尝试多种格式
        formats = [
            "%b %d, %Y",    # "Jan 15, 2026"
            "%B %d, %Y",    # "January 15, 2026"
            "%m/%d/%Y",     # "1/15/2026"
            "%b-%d-%y",     # "Jan-15-26"
            "%d-%b-%Y",     # "15-Jan-2026"
        ]
        for fmt in formats:
            try:
                dt = datetime.strptime(cleaned, fmt)
                return dt.strftime("%Y-%m-%d")
            except ValueError:
                continue

        return None

    @staticmethod
    def parse_listing_type(item_soup: BeautifulSoup) -> str:
        """判断一条记录是竞拍还是一口价。

        判断依据（按优先级）:
        1. 如果包含 "Best offer accepted" → "buy_it_now"
        2. 如果包含 "Buy It Now" 文本   → "buy_it_now"
        3. 如果包含 "bids" 文本          → "auction"
        4. 默认                           → "auction"

        eBay 页面结构中，listing type 信息散布在多个位置，
        所以我们用全文搜索关键词的方式判断。
        """
        text = item_soup.get_text(separator=" ", strip=True).lower()
        if "best offer accepted" in text:
            return "buy_it_now"
        if "buy it now" in text:
            return "buy_it_now"
        if "bid" in text:
            return "auction"
        return "auction"

    def _parse_results_page(self, html: str) -> list[SaleRecord]:
        """解析一整页 eBay 搜索结果 HTML，提取所有已售记录。

        eBay 搜索结果页的 DOM 结构（2025+ 新版 s-card）:
        <ul class="srp-results">
          <li class="s-card">                          ← 每条记录
            <a class="s-card__link" href="...">        ← 商品链接（含 /itm/）
            <span class="s-card__title">               ← 标题
            <span class="s-card__price">               ← 价格
            <span class="positive">                    ← "Sold  Mar 29, 2026"
            <img class="s-card__image">                ← 图片
          旧版使用 li.s-item + s-item__* 选择器，此方法同时兼容两种结构。

        逻辑:
        1. 优先用 li.s-card（新版），回退到 li.s-item（旧版）
        2. 对每个元素提取 url, title, price, date, listing_type
        3. 过滤掉广告（"Shop on eBay"）和缺少关键字段的条目
        4. 返回有效的 SaleRecord 列表
        """
        soup = BeautifulSoup(html, "html.parser")

        # 新版用 s-card，旧版用 s-item，优先新版
        items = soup.select("li.s-card")
        if not items:
            items = soup.select("li.s-item")
        records = []

        for item in items:
            # --- URL ---
            # 查找包含 /itm/ 的链接（真实商品页，排除广告）
            url = None
            for link_tag in item.select("a[href*='/itm/']"):
                url = link_tag.get("href", "")
                break
            if not url:
                continue

            # 去掉 URL 中的跟踪参数，只保留到 item ID
            if "?" in url:
                url = url.split("?")[0]

            # --- 标题 ---
            # 新版: .s-card__title, 旧版: div.s-item__title
            title_tag = (
                item.select_one(".s-card__title")
                or item.select_one("div.s-item__title")
                or item.select_one("span.s-item__title")
            )
            title = title_tag.get_text(strip=True) if title_tag else ""
            # 去掉尾部 "Opens in a new window or tab"
            title = re.sub(r"Opens in.*$", "", title).strip()
            # 跳过广告条目
            if not title or "shop on ebay" in title.lower():
                continue

            # --- 价格 ---
            # 新版: span.s-card__price, 旧版: span.s-item__price
            price_tag = (
                item.select_one("span.s-card__price")
                or item.select_one("span.s-item__price")
            )
            price_text = price_tag.get_text(strip=True) if price_tag else ""
            price = self.parse_price(price_text)
            if price is None:
                continue

            # --- 日期 ---
            # 新版: span.positive 含 "Sold  Mar 29, 2026"
            # 旧版: span.POSITIVE / span.s-item__ended-date
            date_str = None
            for selector in ["span.positive", "span.POSITIVE",
                             "span.s-item__ended-date", "span.s-item__endedDate"]:
                date_tag = item.select_one(selector)
                if date_tag:
                    text = date_tag.get_text(strip=True)
                    if "sold" in text.lower() or "Sold" in text:
                        date_str = self.parse_date(text)
                        if date_str:
                            break

            # 回退: 搜索任何含 "Sold" 的文本节点
            if not date_str:
                for el in item.find_all(string=lambda t: t and "Sold" in t):
                    date_str = self.parse_date(el.strip())
                    if date_str:
                        break

            if not date_str:
                date_str = datetime.now().strftime("%Y-%m-%d")

            # --- Listing 类型 ---
            listing_type = self.parse_listing_type(item)

            # --- 卖家 ---
            seller_tag = (
                item.select_one("span.s-item__seller-info-text")
                or item.select_one(".s-card__seller")
            )
            seller = seller_tag.get_text(strip=True) if seller_tag else ""

            # --- 图片 ---
            img_tag = (
                item.select_one("img.s-card__image")
                or item.select_one("img.s-item__image-img")
            )
            image_url = ""
            if img_tag:
                image_url = img_tag.get("src", "") or img_tag.get("data-defer-load", "")

            records.append(SaleRecord(
                title=title,
                price=price,
                date=date_str,
                listing_type=listing_type,
                url=url,
                seller=seller,
                image_url=image_url,
            ))

        return records

