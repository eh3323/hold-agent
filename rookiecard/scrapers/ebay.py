    # 第 4 层: 数据库持久化
    # ===================================================================

    def save_sales(
        self,
        player_name: str,
        series: str,
        parallel: str,
        grade: str,
        sales: list[SaleRecord],
        db_path: str = None,
    ) -> int:
        """将已售记录写入 card_sales 表。

        逻辑:
        1. 通过 player_name 在 players 表中查找 player_id
           - 如果球员不存在，跳过并发出警告
        2. 对每条 SaleRecord 执行 INSERT OR IGNORE
           - card_sales 表的 UNIQUE(url) 约束自动去重
           - 相同 URL 的记录不会被重复插入
        3. 返回实际新增的行数

        Args:
            player_name: 球员姓名，必须已存在于 players 表
            series:      卡片系列 (写入 card_sales.series)
            parallel:    卡片版本 (写入 card_sales.parallel)
            grade:       评级 (写入 card_sales.grade)
            sales:       SaleRecord 列表
            db_path:     数据库路径（None 则使用默认）

        Returns:
            int: 新增行数
        """
        if not sales:
            return 0

        with get_db(db_path) as conn:
            # 查找 player_id
            row = conn.execute(
                "SELECT id FROM players WHERE name = ?", (player_name,)
            ).fetchone()
            if not row:
                logger.warning("Player '%s' not found in DB, skipping save.", player_name)
                return 0
            player_id = row["id"]

            # 记录插入前的行数，用于计算新增数
            before = conn.execute("SELECT COUNT(*) FROM card_sales").fetchone()[0]

            for sale in sales:
                conn.execute(
                    """INSERT OR IGNORE INTO card_sales
                       (player_id, series, parallel, grade, price, sale_date,
                        listing_type, platform, url)
                       VALUES (?, ?, ?, ?, ?, ?, ?, 'ebay', ?)""",
                    (
                        player_id, series, parallel, grade,
                        sale.price, sale.date, sale.listing_type, sale.url,
                    ),
                )

            after = conn.execute("SELECT COUNT(*) FROM card_sales").fetchone()[0]
            inserted = after - before

        logger.info("Saved %d new sales for %s (skipped %d duplicates).",
                     inserted, player_name, len(sales) - inserted)
        return inserted


