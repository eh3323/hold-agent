    @staticmethod
    def _build_search_url(player: str, series: str = "", parallel: str = "",
                          grade: str = "", page: int = 1) -> str:
        """拼接 eBay 已售列表的搜索 URL。

        搜索词构成: "{player} {series} {parallel} {grade} rookie card"
        URL 参数:
        - _nkw:        搜索关键词（URL 编码）
        - LH_Complete: 1 = 只看已完成的交易
        - LH_Sold:     1 = 只看已售出的商品
        - _sop:        13 = 按结束时间排序（最新在前）
        - _pgn:        页码（从 1 开始）

        Example:
            _build_search_url("Victor Wembanyama", "Prizm", "Silver", "PSA 10")
            → "https://www.ebay.com/sch/i.html?_nkw=Victor+Wembanyama+Prizm+Silver+PSA+10+rookie+card&LH_Complete=1&LH_Sold=1&_sop=13&_pgn=1"
        """
        parts = [player]
        if series:
            parts.append(series)
        if parallel:
            parts.append(parallel)
        if grade:
            parts.append(grade)
        parts.append("rookie card")

        query = quote_plus(" ".join(parts))
        return f"{EBAY_BASE_URL}?_nkw={query}&LH_Complete=1&LH_Sold=1&_sop=13&_pgn={page}"

    @staticmethod
    def _build_search_url_raw(search_query: str, page: int = 1) -> str:
        """用用户提供的精确搜索词构建 eBay 已售列表 URL。

        与 _build_search_url 不同，这里直接使用用户从 eBay 扫描得到的
        精确标题作为搜索词，不做任何拼接，确保搜索结果精准匹配。
        """
        query = quote_plus(search_query)
        return f"{EBAY_BASE_URL}?_nkw={query}&LH_Complete=1&LH_Sold=1&_sop=13&_pgn={page}"

    def get_sold_listings(
        self,
        player: str = "",
        series: str = "",
        parallel: str = "",
        grade: str = "",
        days: int = 30,
        search_query: str = "",
    ) -> list[SaleRecord]:
        """抓取指定球星卡的 eBay 已售记录。这是对外的主入口方法。

        两种搜索模式:
        - 精确模式: 传入 search_query（来自用户扫描 eBay 得到的精确标题），直接用它搜索
        - 组合模式: 传入 player/series/parallel/grade，拼接为搜索词

        逻辑:
        1. 构建搜索 URL
        2. 逐页抓取（page 1, 2, ... 直到 max_pages）
        3. 解析每页得到 SaleRecord 列表
        4. 日期过滤: 只保留最近 days 天内的记录
        5. 终止条件（满足任一即停止翻页）:
           a. 当前页无结果
           b. 当前页最后一条记录已超出 days 天范围
           c. 已达到 max_pages 页数上限

        Args:
            player:       球员姓名 (e.g. "Victor Wembanyama")
            series:       卡片系列 (e.g. "Prizm", "Select")
            parallel:     卡片版本 (e.g. "Silver", "Base")
            grade:        评级 (e.g. "PSA 10", "Raw")
            days:         只获取最近 N 天的成交记录
            search_query: 精确搜索词（优先使用，非空时忽略 player/series/parallel/grade）

        Returns:
            list[SaleRecord] 按日期倒序排列
        """
        cutoff = (datetime.now() - timedelta(days=days)).strftime("%Y-%m-%d")
        all_sales: list[SaleRecord] = []

        for page in range(1, self.max_pages + 1):
            if search_query:
                url = self._build_search_url_raw(search_query, page)
            else:
                url = self._build_search_url(player, series, parallel, grade, page)
            logger.info("Fetching page %d: %s", page, url)

            try:
                html = self._get_page(url)
            except RuntimeError as e:
                logger.error("Failed to fetch page %d: %s", page, e)
                break

            records = self._parse_results_page(html)
            if not records:
                logger.info("No results on page %d, stopping.", page)
                break

            # 日期过滤 + 检查是否超出范围
            page_has_valid = False
            for rec in records:
                if rec.date >= cutoff:
                    all_sales.append(rec)
                    page_has_valid = True

            # 如果本页最后一条记录已超出时间范围，后续页只会更旧，停止翻页
            if not page_has_valid:
                logger.info("All records on page %d are older than %d days, stopping.", page, days)
                break

            logger.info("Page %d: %d records collected.", page, len(records))

        # 按日期倒序排列
        all_sales.sort(key=lambda s: s.date, reverse=True)
        logger.info("Total: %d sales in last %d days.", len(all_sales), days)
        return all_sales

