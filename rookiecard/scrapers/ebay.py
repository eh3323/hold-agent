"""eBay Sold Listings Scraper — 抓取球星卡真实成交价格数据。

第一性目的:
    获取 NBA 球星卡在 eBay 上的已售记录（成交价、日期、拍卖/一口价）。
    这是整个投资 Agent 的"价格眼睛"——后续的价格趋势分析、RSI 计算、
    卖出信号判断全部依赖这里的成交数据。

整体架构:
    EbayScraper 类分为 4 层:
    1. HTTP 请求层 (_get_page)      — 负责发请求，处理反爬、重试、速率控制
    2. HTML 解析层 (_parse_*)       — 负责从 HTML 提取结构化数据
    3. 业务逻辑层 (get_sold_listings) — 负责搜索、翻页、日期过滤
    4. 持久化层   (save_sales)       — 负责写入 card_sales 表并去重

数据流:
    搜索关键词 → 拼接 eBay URL → 逐页抓取 HTML → 解析每条记录
    → 过滤无效/超期条目 → 返回 SaleRecord 列表 → 写入数据库

Usage:
    scraper = EbayScraper()
    sales = scraper.get_sold_listings(
        player="Victor Wembanyama",
        series="Prizm",
        parallel="Silver",
        grade="PSA 10",
        days=30,
    )
    scraper.save_sales("Victor Wembanyama", "Prizm", "Silver", "PSA 10", sales)

CLI:
    python -m rookiecard.scrapers.ebay --player "Victor Wembanyama" --series "Prizm"
"""

