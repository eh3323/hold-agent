@dataclass
class SaleRecord:
    """一条 eBay 已售记录。

    Attributes:
        title:        商品标题（含卡片描述信息）
        price:        成交价，单位 USD
        date:         成交日期，ISO 格式 "2026-01-15"
        listing_type: "auction"（竞拍） 或 "buy_it_now"（一口价）
        url:          eBay 商品页链接，用于去重（card_sales 表的唯一键）
        seller:       卖家 ID
        image_url:    商品图片链接
    """
    title: str
    price: float
    date: str
    listing_type: str
    url: str
    seller: str = ""
    image_url: str = ""


# ---------------------------------------------------------------------------
# 核心类: EbayScraper
# ---------------------------------------------------------------------------


class EbayScraper:
    """eBay 已售列表爬虫。

    设计原则:
    - 每次 HTTP 请求间隔 delay + 随机 0~2 秒，避免触发反爬
    - 随机轮换 User-Agent
    - 使用 httpx.Client 保持会话 Cookie
    - 所有解析方法为 staticmethod，方便单独测试
    """

    def __init__(
        self,
        delay: float | None = None,
        max_pages: int | None = None,
        max_retries: int | None = None,
        timeout: float | None = None,
    ):
        """
        Args:
            delay:       每次请求的最小间隔秒数（默认取 Config.EBAY_DELAY = 3.0）
            max_pages:   最多翻几页（每页约 60 条，3 页 ≈ 180 条）
            max_retries: 单次请求的最大重试次数
        """
        self.delay = delay if delay is not None else Config.EBAY_DELAY
        self.max_pages = max_pages if max_pages is not None else Config.EBAY_MAX_PAGES
        self.max_retries = max_retries if max_retries is not None else Config.EBAY_MAX_RETRIES
        self.timeout = timeout if timeout is not None else Config.EBAY_TIMEOUT
        self._last_request_time = 0.0  # 上次请求的时间戳，用于速率控制
        self._session_warmed = False
        # 整个会话固定一个 UA，避免同 session 内 UA 变化被检测
        self._user_agent = random.choice(USER_AGENTS)
        self._client = self._build_client()

    def _build_client(self) -> curl_requests.Session:
        """构建 curl_cffi Session，模拟 Chrome 的 TLS 指纹（JA3/JA4）。

        curl_cffi 的核心优势：底层使用 curl-impersonate，
        能精确复现 Chrome 的 TLS ClientHello、HTTP/2 SETTINGS 帧、
        HPACK 头压缩顺序等特征，从而绕过基于 TLS 指纹的反爬。
        """
        session = curl_requests.Session(
            impersonate="chrome131",  # 模拟 Chrome 131 的完整 TLS 指纹
            headers=DEFAULT_HEADERS,
            timeout=self.timeout,
        )
        if Config.EBAY_PROXY_URL:
            session.proxies = {
                "http": Config.EBAY_PROXY_URL,
                "https": Config.EBAY_PROXY_URL,
            }
        return session

    def close(self) -> None:
        """显式关闭底层连接池。"""
        self._client.close()

    def __del__(self) -> None:
        try:
            self.close()
        except Exception:
            pass

    def _reset_client(self) -> None:
        """在遇到 503 / challenge 页后重建会话（清除被标记的 cookie + 换新 UA）。"""
        try:
            self._client.close()
        except Exception:
            pass
        self._user_agent = random.choice(USER_AGENTS)
        self._client = self._build_client()
        self._session_warmed = False

    def _build_headers(self, referer: str | None = None) -> dict[str, str]:
        """构建单次请求头，保持会话内 UA 一致。"""
        headers = {
            "User-Agent": self._user_agent,
            "Referer": referer or EBAY_HOME_URL,
        }
        return headers

    @staticmethod
    def _looks_blocked(html: str) -> bool:
        """检测是否命中了 challenge / 反爬页面。"""
        if not html:
            return False
        lowered = html.lower()
        return any(pattern in lowered for pattern in BLOCKED_PAGE_PATTERNS)

    def _warm_up_session(self) -> None:
        """先访问首页获取 cookie，模拟真实用户从首页进入搜索的行为。"""
        if self._session_warmed:
            return
        try:
            # 首页请求用 Sec-Fetch-Site: none（直接在地址栏打开）
            warmup_headers = self._build_headers()
            warmup_headers["Sec-Fetch-Site"] = "none"
            resp = self._client.get(
                EBAY_HOME_URL,
                headers=warmup_headers,
                allow_redirects=True,
            )
            self._last_request_time = time.time()
            resp.raise_for_status()
            self._session_warmed = True
            # warm-up 后等一下，模拟用户浏览首页后再搜索
            time.sleep(random.uniform(1.5, 3.0))
        except Exception as exc:
            logger.debug("eBay session warm-up failed: %s", exc)

    def _get_page_with_playwright(self, url: str) -> str:
        """在 curl_cffi 连续失败后，用真实浏览器 + stealth 插件回退抓取一次。"""
        try:
            from playwright.sync_api import TimeoutError as PlaywrightTimeoutError
            from playwright.sync_api import sync_playwright
        except ImportError as exc:
            raise RuntimeError("Playwright fallback is not installed") from exc

        try:
            from playwright_stealth import stealth_sync
        except ImportError:
            stealth_sync = None

        try:
            with sync_playwright() as playwright:
                launch_kwargs = {
                    "headless": True,
                    "args": [
                        "--disable-blink-features=AutomationControlled",
                        "--no-sandbox",
                    ],
                }
                if Config.EBAY_PLAYWRIGHT_PROXY_URL:
                    launch_kwargs["proxy"] = {"server": Config.EBAY_PLAYWRIGHT_PROXY_URL}

                browser = playwright.chromium.launch(**launch_kwargs)
                try:
                    context = browser.new_context(
                        user_agent=random.choice(USER_AGENTS),
                        locale="en-US",
                        viewport={"width": 1440, "height": 900},
                        extra_http_headers={"Accept-Language": "en-US,en;q=0.9"},
                    )
                    try:
                        page = context.new_page()

                        # 注入 stealth 脚本：隐藏 navigator.webdriver、
                        # 伪造 plugins/languages/WebGL 等浏览器指纹
                        if stealth_sync is not None:
                            stealth_sync(page)

                        timeout_ms = int(max(self.timeout, 20.0) * 1000)

                        page.goto(EBAY_HOME_URL, wait_until="domcontentloaded", timeout=timeout_ms)
                        page.wait_for_timeout(1500)

                        response = page.goto(url, wait_until="domcontentloaded", timeout=timeout_ms)
                        page.wait_for_timeout(2500)

                        if response and response.status in {429, 500, 502, 503, 504}:
                            raise RuntimeError(f"Playwright got HTTP {response.status}")

                        html = page.content()
                        if self._looks_blocked(html):
                            raise RuntimeError("Playwright also hit an anti-bot challenge")

                        return html
                    finally:
                        context.close()
                finally:
                    browser.close()
        except PlaywrightTimeoutError as exc:
            raise RuntimeError(f"Playwright timed out for {url}") from exc

    # ===================================================================
    # 第 1 层: HTTP 请求
    # ===================================================================

    def _get_page(self, url: str) -> str:
        """发送 HTTP GET 请求并返回 HTML 文本。

        逻辑:
        1. 计算距上次请求的时间差，不足 delay 则 sleep 补齐
        2. 在 delay 基础上加 0~2 秒随机抖动，模拟人类行为
        3. 随机选一个 User-Agent
        4. 用 curl_cffi 发请求（模拟 Chrome TLS 指纹、自动跟随重定向、保持 Cookie）
        5. 失败时指数退避重试

        Returns:
            HTML 文本字符串

        Raises:
            RuntimeError: 重试耗尽后仍失败
        """
        from curl_cffi import CurlError

        self._warm_up_session()
        last_error: Exception | None = None

        for attempt in range(self.max_retries):
            try:
                # --- 速率控制 ---
                elapsed = time.time() - self._last_request_time
                wait = self.delay - elapsed + random.uniform(0, 2)
                if wait > 0:
                    time.sleep(wait)

                resp = self._client.get(
                    url,
                    headers=self._build_headers(referer=EBAY_HOME_URL),
                    allow_redirects=True,
                )
                self._last_request_time = time.time()

                if resp.status_code in {429, 500, 502, 503, 504}:
                    raise RuntimeError(f"transient HTTP status {resp.status_code}")

                resp.raise_for_status()

                if self._looks_blocked(resp.text):
                    raise RuntimeError("eBay anti-bot challenge detected")

                return resp.text

            except CurlError as e:
                # curl_cffi 的网络/超时错误（对应 httpx 的 TimeoutException/NetworkError）
                last_error = e
                backoff = self.delay + (2 ** attempt) + random.uniform(1, 3)
                logger.warning(
                    "eBay request failed (attempt %d/%d): %s. Retrying in %.1fs...",
                    attempt + 1, self.max_retries, e, backoff,
                )
                self._reset_client()
                time.sleep(backoff)
            except RuntimeError as e:
                last_error = e
                backoff = self.delay + (2 ** attempt) + random.uniform(3, 8)
                logger.warning(
                    "eBay error (attempt %d/%d): %s. Retrying in %.1fs...",
                    attempt + 1, self.max_retries, e, backoff,
                )
                self._reset_client()
                time.sleep(backoff)

        if Config.EBAY_USE_PLAYWRIGHT_FALLBACK:
            try:
                logger.info("curl_cffi exhausted, trying Playwright+stealth fallback for %s", url)
                return self._get_page_with_playwright(url)
            except Exception as fallback_error:
                raise RuntimeError(
                    f"Failed to fetch {url} after {self.max_retries} retries; "
                    f"last_error={last_error}; playwright_fallback={fallback_error}"
                ) from fallback_error

        raise RuntimeError(f"Failed to fetch {url} after {self.max_retries} retries; last_error={last_error}")

